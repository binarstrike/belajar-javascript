/**
 * operator logika
 * && = and
 * || = or
 * ! = not atau negasi(kebalikan)
 * 
 * digunakan untuk membadingkan nilai dari sebuah variabel dengan tipe data boolean
 */
 var x = 10;
 var y = 11;
 
 var a = x < y; // true
 var b = x > y; // false
 
 console.log(a && b);
 console.log(a || b);
 console.log(!a)